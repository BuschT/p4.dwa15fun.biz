	Welcome To QuizTastic!<br /><br />
	QuizTastic! is a testing system designed mainly for use on private systems such as company Intranets or within groups of friends. It can be used to test serious subject matter or just as a fun brain teaser.<br /><br />
	Start by looking at some tests others have created, or create one of your own. Each test may have up to 5 questions. Once you have taken a test, you can view your score. You cannot take the test again! All tests are saved forever for record-keeping purposes.
